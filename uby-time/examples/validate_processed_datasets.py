#!/usr/bin/env python3
"""
Validate processed UBY-annotated datasets against dataset profiles.

This script is intended for larger real-data conformance validation.  It scans
processed CSV outputs that were generated by the example annotation pipelines and
checks them against dataset-specific profiles.
"""

from __future__ import annotations

import csv
import json
import sys
import time
from dataclasses import asdict
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from uby_time.dataset_profiles import (
    NASA_EXOPLANET_ARCHIVE_PROFILE,
    PBDB_DINOSAURIA_PROFILE,
    USGS_EARTHQUAKE_PROFILE,
    DatasetProfile,
    validate_dataset_records,
)

PROCESSED_DIR = ROOT / "data" / "processed"
REPORT_OUT = PROCESSED_DIR / "processed_datasets_conformance_report.json"

DATASETS: tuple[tuple[str, Path, DatasetProfile], ...] = (
    ("pbdb_dinosauria_uby", PROCESSED_DIR / "pbdb_dinosauria_uby.csv", PBDB_DINOSAURIA_PROFILE),
    (
        "nasa_exoplanet_archive_uby",
        PROCESSED_DIR / "nasa_exoplanet_archive_uby.csv",
        NASA_EXOPLANET_ARCHIVE_PROFILE,
    ),
    (
        "usgs_earthquakes_uby_benchmark",
        PROCESSED_DIR / "usgs_earthquakes_uby_benchmark.csv",
        USGS_EARTHQUAKE_PROFILE,
    ),
)


def _load_csv(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8-sig", newline="") as file:
        return list(csv.DictReader(file))


def main() -> int:
    PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    wall_start = time.perf_counter()

    results = []
    total_records = 0
    total_errors = 0
    total_warnings = 0

    for dataset_name, path, profile in DATASETS:
        if not path.exists():
            results.append(
                {
                    "dataset": dataset_name,
                    "source_file": str(path.as_posix()),
                    "profile_id": profile.profile_id,
                    "exists": False,
                    "record_count": 0,
                    "error_count": 0,
                    "warning_count": 0,
                    "skipped_count": 0,
                    "sample_messages": [
                        {
                            "code": "DATASET_FILE_MISSING",
                            "level": "error",
                            "message": f"Processed dataset file not found: {path}",
                        }
                    ],
                }
            )
            total_errors += 1
            continue

        rows = _load_csv(path)
        summary, messages = validate_dataset_records(rows, profile)
        total_records += summary.record_count
        total_errors += summary.error_count
        total_warnings += summary.warning_count

        results.append(
            {
                "dataset": dataset_name,
                "source_file": str(path.as_posix()),
                "profile_id": profile.profile_id,
                "exists": True,
                "record_count": summary.record_count,
                "error_count": summary.error_count,
                "warning_count": summary.warning_count,
                "skipped_count": summary.skipped_count,
                "sample_messages": [asdict(message) for message in messages[:20]],
            }
        )

    wall_seconds = time.perf_counter() - wall_start
    report = {
        "generated_by": "uby-time/0.1.0",
        "report_type": "processed_dataset_conformance_validation",
        "total_records": total_records,
        "total_errors": total_errors,
        "total_warnings": total_warnings,
        "wall_seconds": wall_seconds,
        "records_per_second": total_records / wall_seconds if wall_seconds else 0,
        "datasets": results,
    }
    REPORT_OUT.write_text(json.dumps(report, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"Validated records: {total_records}")
    print(f"Total errors: {total_errors}")
    print(f"Total warnings: {total_warnings}")
    print(f"Wall seconds: {wall_seconds:.6f}")
    print(f"Records/s: {report['records_per_second']:.2f}")
    print(f"Report: {REPORT_OUT}")

    for result in results:
        print(
            f"- {result['dataset']}: records={result['record_count']} "
            f"errors={result['error_count']} warnings={result['warning_count']}"
        )

    return 1 if total_errors else 0


if __name__ == "__main__":
    raise SystemExit(main())
